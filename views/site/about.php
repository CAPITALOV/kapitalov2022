<p><a href="http://www.asx.com.au/"><img height="80" src="http://capitalov.com/img-header-logo.png" style="border-width:0px;" width="183" /></a><br />
    <a href="http://www.asx.com.au/" target="_blank">Australian Stock Exchange</a><br />
    Австралийская фондовая биржа (Australian Stock Exchange, ASX) (ASX: ASX) расположена в Сиднее. Данная биржа образована в результате слияния фондовых бирж в Аделаиде, Брисбене, Хобарте, Мельбурне и Перте. В 1984 г. решением биржи отменены фиксированные комиссионные, а в октябре 1990 г. введены полностью автоматизированные торги.&nbsp;<br />
    <br />
    <a href="http://www.tse.or.jp/"><img height="264" src="http://capitalov.com/logo.gif" style="border-width:0px;" width="207" /></a><br />
    <a href="http://www.tse.or.jp/" target="_blank">Tokyo Stock Exchange&nbsp;</a><br />
    Токийская Фондовая Биржа (Tokyo Stock Exchange) была основана в 1878 году На долю токийской биржи приходится свыше 80 % биржевого оборота страны. Основными продавцами и покупателями являются институциональные владельцы ценных бумаг. В Японии индивидуальные владельцы имеют лишь 20 % обращающихся на бирже акций, а 80 % принадлежит финансовым учреждениям, страховым компаниям и корпорациям. Владельцы акций рассчитывают не на дивиденды (в начале 1990 г. они были в 70 раз меньше рыночной стоимости акций), а на повышение курса акций и получение дохода в результате продажи акций по более высокому курсу (среднегодовой доход на проданную акцию выпуска 1980&mdash;1994 гг. составил 34,7 %). Основной метод торговли &mdash; открытый двойной аукцион, торговля с голоса в биржевой толпе &laquo;зараба&raquo;. До 80 % всех обращающихся в стране акций продается и покупается на Токийской фондовой бирже. Из 1,3-1,5 млн имеющихся в Японии акционерных фирм здесь зарегистрировано 1517, но на них приходится более 25 % всех производимых товаров и услуг. В 2005 году Токийская Фондовая Биржа заняла почетное второе место в мировой классификации крупнейших фондовых рынков мира. В ее состав вошли 2,5 тыс. компаний, приносящих ежегодно более 3000 млрд долл. дохода.&nbsp;<br />
