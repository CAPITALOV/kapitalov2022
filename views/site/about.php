<p><a target="_blank" href="http://nasdaq.com/">NASDAQ</a><br />
    (National Association of Securities Dealers Automated Quotations) - система компьютеризированной внебиржевой котировки акций в США с капитализацией эмитентов около $4 трлн. Торгуются акции 3200 компаний из 37 стран, преимущественно из сферы био- и информтехнологий, медиа, финансовых услуг и ритейла. Доступны около 500 финансовых инструментов. Биржевые фонды ETF (exchange traded fund) на индексы NASDAQ-100 (входит 100 наиболее быстро растущих компаний), NASDAQ-100 Technology Sector Index, NASDAQ Biotechnology Index, на The Bank of New York ADR Index (рассчитывается на основе депозитарных расписок международных эмитентов), а также на индексы отдельных стран. Торгуются депозитарные расписки &quot;СТС Медиа&quot; и &quot;Голден Телеком&quot;.&nbsp;<br />
