    <div>Группа РТС насчитывает более чем 10-летнюю историю, которая началась в 1995 году с запуском торгов ценными бумагами на Классическом рынке РТС. Основные принципы торговли на Классическом рынке &mdash; возможность заключать сделки, выбирая валюту, способ и время расчетов. С самого начала торгов в 1995 году ОАО &quot;Фондовая биржа РТС&quot; была построена на базе неанонимных котировок без предварительного депонирования ценных бумаг и денежных средств, что позволяет организовать торги по максимально широкому спектру ценных бумаг. На основании цен, формирующихся на Классическом рынке акций, с 1995 года рассчитывается Индекс РТС. Это основной индикатор фондового рынка России, признанный не только в России, но и за рубежом. Методика его расчета постоянно совершенствуется, что позволяет ему сохранять лидирующие позиции ведущего индикатора деловой активности России. Индекс РТС рассчитывается на основании котировок 50 ценных бумаг наиболее капитализированных российских компаний. В феврале 2001 г. была введена в эксплуатацию система RTS Board &mdash; информационная система Фондовой биржи РТС предназначенная для индикативного котирования ценных бумаг, не допущенных к торгам в РТС. В RTS Board можно выставлять котировки по 1780 бумагам 1344 эмитентов. Также в 2001 году начал работу Срочный рынок РТС FORTS. FORTS является лидирующей площадкой в России по торговле производными финансовыми инструментами. Использование фьючерсов и опционов предоставляет инвесторам возможность реализовывать различные стратегии: осуществлять спекулятивные операции, хеджировать риски, проводить арбитраж, эффективно управлять портфелем акций с помощью фьючерса на Индекс РТС, который в 2007 году стал самым ликвидным инструментом фондового рынка России. 23 ноября 2004 года начались торги на Биржевом рынке РТС. Здесь торги ценными бумагами проводятся в режиме анонимной торговли с использованием принципа непрерывного двойного аукциона встречных заявок (Order-Driven Market), по технологии &laquo;поставка против платежа&raquo; со 100 % предварительным депонированием активов и расчетами в рублях. Приоритетными направлениями деятельности Биржевого рынка РТС являются повышение ликвидности рублевого сегмента рынка ценных бумаг с использованием механизма действенного контроля со стороны широкого круга профессиональных участников рынка ценных бумаг и развитие рынка акций &laquo;второго эшелона&raquo;. 15 ноября 2005 года был запущен режим анонимной электронной торговли. Здесь торгуются наиболее ликвидные акции Классического рынка РТС. В 2007 году начала работу площадка для компаний малой и средней капитализации RTS START. Открытое акционерное общество &laquo;Фондовая биржа РТС&raquo; единственная биржевая площадка в России, существующая в форме ОАО ,и, акций которой свободно обращаются на рынке. В 2008 году стартовал новый, не имеющий аналогов в России проект ОАО &laquo;Фондовая биржа РТС&raquo; - RTS Global.</div>
